int		ft_atoi(const char *str)
{
	int		neg;
	long	nbr;

	nbr = 0;
	neg = 0;
	while ((*str <= 13 && *str >= 9) || *str == 32)
		str++;
	if (*str == '+' || *str == '-')
		if (*str++ == '-')
			neg = 1;
	while (*str >= '0' && *str <= '9')
	{
		nbr = 10 * nbr + (*str++ - '0');
		if (nbr > 9223372036854775807 || nbr < -9223372036854775807)
			return (0);
	}
	(neg) ? nbr *= -1 : nbr;
	return (nbr);
}
